//for the constant we use const keyword
const myName="abhishek kaushik";

//for variable we have 2 option's->let[have scope boundaries]/var[doesn't support scope boundaries]
var myMarks=123;

let myVillage="kallarpur rajput";

console.log(myMarks);
console.log(myName);
console.log(myVillage);

//another way to print all variables once using console.table([])
console.table([myMarks,myName,myVillage]);